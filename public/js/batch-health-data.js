$(document).ready(function(){
    $("#btn-batchdata").click(function(){
        $(".batch-student-health-data").fadeIn(500);
    });
    $("#btn-cancel").click(function(){
        $(".batch-student-health-data").fadeOut(500);
    });
    $("#btn-create").click(function(){
        $(".batch-student-health-data").fadeOut(500);
    });

    
    $("#btn-batchdata").click(function(){
        $(".batch-personel-health-data").fadeIn(500);
    });
    $("#btn-cancel").click(function(){
        $(".batch-personel-health-data").fadeOut(500);
    });
    $("#btn-create").click(function(){
        $(".batch-personel-health-data").fadeOut(500);
    });
})
